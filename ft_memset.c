#include <stdio.h>
#include <string.h>


void	*ft_memset(void *s, int c, size_t n)
{
	size_t i = 0;
	unsigned char *ptr = (unsigned char *)s;
    while(n--)
	{
		ptr[i] = (unsigned char)c;
		i++;
	}

	return s;

}


